package com.brian.dmgnt.models;

public class FirstAid {

    private String id, name, aidInfo;

    public FirstAid() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAidInfo() {
        return aidInfo;
    }

    public void setAidInfo(String aidInfo) {
        this.aidInfo = aidInfo;
    }
}
